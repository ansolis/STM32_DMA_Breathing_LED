// The DMA and power management portion of the code is adapted from:
// https://electronics.stackexchange.com/questions/547695/stm32-how-to-trigger-a-dma-transfer-via-hardware-timer-while-cpu-is-in-sleep
// as of 1/2/2023 version based on the answer by PauLEffect on 2/11/2021
// edited by Tagli on 8/14/2021
//
// CubeMx Setup
// 1. CPU system clock is set to 100MHz
// 2. TIM1 is set to roll over 10 times a second with:
//    - Clock Source set to Internal clock
//    - Prescaler set to 199
//    - Counter Period set to 49999 resulting in rollover frequency of
//      (100Mhz/200)/50000 = 10 Hz (100ms period)
//    - DMA request configured for:
//      - TIM1_UP request type (timer update on rollover)
//      - Memory to Peripheral direction
//      - Circular mode
//      - Increment address for memory only
//      - Data width set to Half Word
// 3. TIM4 is set to generate PWM with 1ms period and 50% initial duty cycle:
//    - Clock Source set to Internal clock
//    - Channel1 set to PWM Generation CH1
//    - Prescaler set to 99
//    - Counter Period set to 999 resulting in rollover frequency of
//      (100Mhz/100)/1000 = 1000 Hz (1ms period)
//    - Make sure the "auto-reload preload" is NOT enabled. If it is, the CCR1
//      register will not get updated - only its shadow register will.
//    - Pulse set to 500 (half of Counter Period value resulting in 50% PWM)
// 4. Pin PD12, which is connected to the green LED on the STM32F4DISCOVERY board,
//    is assigned alternative function of TIM4_CH1 to route the PWM output
//    of the timer channel 1 to the green LED.
//

