  MX_GPIO_Init();
  MX_DMA_Init();
  MX_TIM1_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */

  // Start PWM generation on PD12 pin via TIM4_CH1 timer output
  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_1);

  // Array of PWM duty cycle values that will be transferred to
  // TIM4 CCR1 register via DMA to control the PWM duty cycle.
  // The transfer will wrap around to the beginning of the array
  // after reaching the end.
  uint16_t pwmArr[10] = {0, 300, 500, 700, 850, 1000, 850, 700, 500, 300};

  // Configure DMA transfer from memory to TIM4 CCR1 register, which
  // controls the PWM duty cycle, on TIM1 update
